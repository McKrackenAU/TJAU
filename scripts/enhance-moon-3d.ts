/**
 * Enhance Moon with 3D Lifelike Quality
 * Matching The Fool's otherworldly dimensional depth
 */

import fs from 'fs';
import path from 'path';

const HUGGINGFACE_API_TOKEN = process.env.HUGGINGFACE_API_TOKEN;

if (!HUGGINGFACE_API_TOKEN) {
  console.error('HUGGINGFACE_API_TOKEN is required');
  process.exit(1);
}

async function enhance3DMoon(): Promise<boolean> {
  try {
    console.log('🎨 Enhancing The Moon with 3D lifelike depth...');
    
    const outputDir = path.join(process.cwd(), 'public', 'authentic-cards', 'major-arcana');
    const outputPath = path.join(outputDir, '18-moon.png');
    
    const prompt = `The Moon tarot card with incredible photorealistic 3D depth and lifelike presence exactly like The Fool card. A mystical full moon with a serene face rendered in complete photorealistic depth, glowing brilliantly in the cosmic sky with realistic lunar textures and natural lighting. Two towers of starlight stand on either side extending into genuine 3D depth with dimensional architecture. A winding path made of liquid moonlight flows with realistic physics through the cosmic landscape. A cosmic wolf and ethereal dog with flowing liquid starlight fur howl peacefully at the moon, rendered with photorealistic musculature and natural movement. A mystical crayfish made of starlight emerges from a pool of cosmic water with genuine depth reflection and realistic water physics. Gentle dewdrops fall like liquid starlight through 3D space. Background has soft purple and pink cosmic clouds with layered atmospheric perspective stretching to infinity. Natural moonbeams cast dimensional shadows and realistic lighting effects. Ultra-ethereal style with genuine 3D lifelike quality matching The Fool's dimensional realism.`;

    console.log('📡 Generating with enhanced 3D parameters...');
    
    const response = await fetch(
      "https://api-inference.huggingface.co/models/stabilityai/stable-diffusion-xl-base-1.0",
      {
        method: "POST",
        headers: {
          "Authorization": `Bearer ${HUGGINGFACE_API_TOKEN}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          inputs: prompt,
          parameters: {
            negative_prompt: "flat, 2D, cartoon, anime, illustration, drawing, sketch, painting, unrealistic, artificial, low quality, blurry, distorted, ugly, deformed, text, watermark, signature, frame, border",
            num_inference_steps: 40,
            guidance_scale: 9.0,
            width: 512,
            height: 768
          }
        }),
      }
    );

    if (!response.ok) {
      const errorText = await response.text();
      console.error(`❌ API Error:`, response.status, errorText);
      return false;
    }

    const imageBuffer = await response.arrayBuffer();
    
    if (imageBuffer.byteLength === 0) {
      console.error(`❌ Empty image buffer`);
      return false;
    }

    fs.writeFileSync(outputPath, Buffer.from(imageBuffer));
    
    console.log(`✨ Successfully enhanced The Moon with 3D depth!`);
    console.log(`📁 Saved to: ${outputPath}`);
    
    return true;
    
  } catch (error) {
    console.error(`❌ Error:`, error);
    return false;
  }
}

async function main() {
  console.log('🌟 Enhancing The Moon with 3D lifelike depth...');
  
  const success = await enhance3DMoon();
  
  if (success) {
    console.log('🎉 Moon enhancement completed!');
  } else {
    console.log('❌ Enhancement failed');
    process.exit(1);
  }
}

main().catch(error => {
  console.error('💥 Script failed:', error);
  process.exit(1);
});